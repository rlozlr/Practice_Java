package quest;

public class SumInt {

	public static void main(String[] args) {
		/* 국어점수(kor), 영어점수(eng), 수학점수(math) 변수를 선언하고 값을 입력하여
		 * 세 점수의 합을 콘솔에 출력
		 */
		
		int kor = 98;
		int eng = 67;
		int math = 62;
		
		int sum = kor + eng + math;
		
		System.out.println(sum);
	}

}
